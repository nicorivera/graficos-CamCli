function load_data() {
  return new Promise((resolve, reject) => {
    Papa.parse("./data/data.csv", {
      download: true,
      header: true,
      complete: function (results) {
        const excludedCodes = ["OWID_KOS", "OWID_USS", "OWID_WRL"];

        const data = results.data.map((fila) => {
          if (excludedCodes.includes(fila.iso3)) {
            return null;
          }
          if (fila.anio) {
            fila.year = fila.anio;
            delete fila.anio;
          }
          if (fila.valor_en_twh) {
            fila.value = parseFloat(fila.valor_en_twh);
            delete fila.valor_en_twh;
          }
          if (fila.iso3) {
            fila.iso3 = fila.iso3;

            const countryData = data_country.find(
              (country) => country.iso3 === fila.iso3
            );

            if (countryData) {
              fila.iso2 = countryData.iso2;
              fila.name = countryData.name;
            }
          }
          return fila;
        });

        let processedData = {};

        data.forEach((item) => {
          if (item) {
            if (!processedData[item.iso2]) {
              processedData[item.iso2] = {
                id: item.iso2,
                value: 0,
                years: [],
              };
            }
            processedData[item.iso2].value += item.value;
            processedData[item.iso2].years.push({
              year: item.year,
              value: item.value,
            });
          }
        });

        console.log("Data", processedData);
        resolve(processedData);
      },
      error: function (error) {
        console.error("Error al cargar o analizar el archivo:", error);
        reject(error);
      },
    });
  });
}
